function display(data){
   product.innerHTML="";
    data.forEach(function(el,i,arr){
        let div = document.createElement("div");
    
        let imageD= document.createElement("img");
        // imageD.setAttribute("src",el.image);
        imageD.src = el.image
    
        let name= document.createElement("h2");
        name.innerText = el.name;
        let price= document.createElement("h3");
        price.innerText= el.price;
        let discount= document.createElement("p");
        discount.innerText = el.discount;
        discount.style.color="red";

        let addCart = document.createElement("button")
        addCart.innerText = "Add Cart";
    
        div.append(imageD,name,price,discount,addCart);
    
        product.append(div);
        addCart.addEventListener("click",function(){
           
            let flag = true;
            for(let i=0; i<LS.length; i++)
            {
                let ele = LS[i]
                if(ele.id===el.id)
                {
                    flag = false;
                }
            }
            if(flag)
            {
                LS.push(el);
                localStorage.setItem("Bookmark",JSON.stringify(LS));
            }
            
        })
    })


    
}